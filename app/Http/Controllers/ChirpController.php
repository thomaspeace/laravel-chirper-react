    /**
     * Store a newly created resource in storage.
     */
    /**
     * Laravel validates to make sure the message won't exceed 255 char limit of the database column
     * The will belong to the logged in user via a chirps relationship
     * The RedirectResponse will reload the chirps.index route
     */
    public function store(Request $request): RedirectResponse
    {
        $validated = $request->validate([
            'message' => 'required|string|max:255',
        ]);
 
        $request->user()->chirps()->create($validated);
 
        return redirect(route('chirps.index'));
    }

